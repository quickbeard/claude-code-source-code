// scripts/generate-sdk-types.ts
// Generates TypeScript types from Zod schemas in coreSchemas.ts
// Usage: bun scripts/generate-sdk-types.ts

import { readFileSync, writeFileSync } from 'fs'
import { resolve, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dir =
  (import.meta as any).dir ??
  (import.meta as any).dirname ??
  dirname(fileURLToPath(import.meta.url))

const ROOT = resolve(__dir, '..')
const schemasPath = resolve(ROOT, 'src/entrypoints/sdk/coreSchemas.ts')
const outputPath = resolve(ROOT, 'src/entrypoints/sdk/coreTypes.generated.ts')

// Read the schemas file
const schemasContent = readFileSync(schemasPath, 'utf-8')

// Extract all schema names (e.g., ModelUsageSchema -> ModelUsage)
const schemaRegex = /export const (\w+Schema) = lazySchema/g
const schemaNames: string[] = []
let match: RegExpExecArray | null

while ((match = schemaRegex.exec(schemasContent)) !== null) {
  schemaNames.push(match[1])
}

// Generate the types file
const typeLines: string[] = [
  '// AUTO-GENERATED FILE - DO NOT EDIT DIRECTLY',
  '// Generated by: bun scripts/generate-sdk-types.ts',
  '// Source: src/entrypoints/sdk/coreSchemas.ts',
  '',
  "import { z } from 'zod/v4'",
  "import * as schemas from './coreSchemas.js'",
  '',
]

for (const schemaName of schemaNames) {
  // Convert FooBarSchema -> FooBar
  const typeName = schemaName.replace(/Schema$/, '')
  typeLines.push(
    `export type ${typeName} = z.infer<ReturnType<typeof schemas.${schemaName}>>`
  )
}

typeLines.push('') // trailing newline

writeFileSync(outputPath, typeLines.join('\n'))
console.log(`Generated ${schemaNames.length} types -> ${outputPath}`)
